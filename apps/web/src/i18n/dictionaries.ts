/**
 * Recursively widen literal types to their primitives so translations only need
 * to match the *shape* of `en`, not its exact string values. Functions are kept
 * intact so per-locale interpolation (e.g. `filling`, `welcomeBack`) still type-checks.
 */
type Widen<T> = T extends (...args: infer A) => infer R
  ? (...args: A) => R
  : T extends string
    ? string
    : T extends number
      ? number
      : T extends readonly (infer U)[]
        ? readonly Widen<U>[]
        : { -readonly [K in keyof T]: Widen<T[K]> }

